package com.example.interaccion;

public class Coordenadas {
    private float coorx;
    private float coory;
public Coordenadas(float coorx, float coory){
    this.coorx=coorx;
    this.coory=coory;
}
    public float getCoorx() {
        return coorx;
    }
    public void setCoorx(float coorx) {
        this.coorx = coorx;
    }
    public float getCoory() {
        return coory;
    }
    public void setCoory(float coory) {
        this.coory = coory;
    }
}
